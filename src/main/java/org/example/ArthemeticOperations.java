package org.example;

public class ArthemeticOperations {
    public static void main(String args[]){
        int a=10;
        int b=20;

        int sum=a+b;
        System.out.println("Addition of a+b= "+sum);


        int minus=a-b;
        System.out.println("Addition of a-b= "+minus);


        int div=a%b;
        System.out.println("Addition of a%b= "+div);


        int mult=a*b;
        System.out.println("Addition of a+b= "+mult);

    }
}
